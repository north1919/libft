#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	a;

	a = ft_strlen(s);
	while (a >= 0)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
		a--;
	}
	return (0);
}
