#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	int		b;
	char	*dest;

	i = 0;
	j = 0;
	b = ft_strlen(s1) + ft_strlen(s2);
	dest = (char *)malloc(sizeof(char) * (b + 1));
	if (dest == NULL)
		return (NULL);
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		dest[i + j] = s2[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
